// swapping using Temp variable
public class Swap{
	public static void main(String[] args)
	{
		int a=100;
		int b=55;
		
		System.out.println("a:"+a);
		System.out.println("b:"+b);
		System.out.println("***************");
		
		
		int temp=a;
		a=b;
		b=temp;
		
		System.out.println("a:"+a);
		System.out.println("b:"+b);
	}
}
		